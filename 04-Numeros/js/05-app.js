let puntaje = 5;

console.log(puntaje++); // Muestra 5 y lo incrementa después de mostrarlo a 6

// Añadir un valor a la variable
puntaje += 3; // 9
puntaje += 5; // 14

// Restar un valor a la variable
puntaje -= 4; // 10

// Multiplicar por un valor a la variable
puntaje *= 3; // 30

// Dividir por un valor
puntaje /=2; // 15

// Módulo
puntaje %= 4; // 3

console.log(puntaje);